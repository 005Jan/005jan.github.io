// Funció principal per canviar l'idioma
function setLanguage(lang) {
    // 1. Guardar l'preferència a localStorage
    localStorage.setItem('cv-lang', lang);
    document.documentElement.lang = lang; // Canviar atribut lang del HTML

    // 2. Traduir textos normals (innerText)
    const elementsToTranslate = document.querySelectorAll('[data-i18n]');
    elementsToTranslate.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            element.innerHTML = translations[lang][key];
            // Fem servir innerHTML perquè hi ha etiquetes <strong> en alguns textos
        }
    });

    // 3. Traduir estructures HTML complexes (llistes UL/LI)
    const htmlElementsToTranslate = document.querySelectorAll('[data-i18n-html]');
    htmlElementsToTranslate.forEach(element => {
        const key = element.getAttribute('data-i18n-html');
        if (translations[lang] && translations[lang][key]) {
            element.innerHTML = translations[lang][key];
        }
    });

    // 4. Actualitzar estat dels botons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
}

// Iniciar l'aplicació
document.addEventListener('DOMContentLoaded', () => {
    // Comprovar si hi ha un idioma guardat prèviament o usar 'ca' per defecte
    const savedLang = localStorage.getItem('cv-lang') || 'ca';
    setLanguage(savedLang);

    // Afegir event listeners als botons
    const langBtns = document.querySelectorAll('.lang-btn');
    langBtns.forEach(btn => {
        btn.addEventListener('click', (e) => {
            const lang = e.target.getAttribute('data-lang');
            setLanguage(lang);
        });
    });
});
